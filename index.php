                    <div class="row">
                        <div class="large-6 medium-6 columns">
                            <p>Born in Athens, Pantelis grew up in Nea Ionia, an Athenian suburb. He initially worked in
                                the Ηellenic Navy, but abandoned this to pursue a musical career. He was a
                                self-taught musician who became known from some YouTube videos. Soon he passed to
                                discography and released four albums with great commercial success. He collaborated with
                                Greek artists such as Vasilis Karras, Despina Vandi and others. He won three awards at
                                the MAD Video Music Awards, first in 2013 as the best new artist the secondly in 2015
                                as the best Greek male singer, and the third one was after his death as the best
                                Greek male singer in 2016, received by his family and his friends. </p>
                            <p>He died on 18 February 2016 in a car accident on Vouliagmenis Avenue, Athens. It
                                was later revealed that he had heavily drunk.</p>

                        </div>
                        <div class="large-6 medium-6 columns">
                            <p>After his death, two of his songs reached the #1 spot on the Billboard chart "Greece
                                Digital Songs". The two songs being "Ta Shinia Sou" and "Pino Apo 'Ki Psila Gia Sena".
                                On 25 of June a cd single was released with 1 unpublished song and 1 song that was only
                                available on videoclip (Was planned to be in the new cd). The song Thimamai in YouTube
                                had 16.000.000+ views within 3 months. On 14 December 2016, his pre-recorded song "Alli
                                Mia Efkeria" was released via artist's official YouTube channel and gained over 1,1
                                million views in 2 days after the release. On 18 February 2017, one year after his
                                death, an other unpublished song of the artist has been released ("Karavia Sto
                                Vitho").</p>
